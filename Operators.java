public class Operators{
 public static void main(String args[])
{
  int num1=25;
  //post increment
  int num2=num1++;
  System.out.println(num1+" "+num2);
  //pre increment
  num2=++num1;
  System.out.println(num1+" "+num2);
  //pre decrement
  num1=--num2;
  System.out.println(num1+" "+num2);
  //post decrement
  num2=num2--;
  System.out.println(num1+" "+num2);
  char ch1='l';
  char ch2=ch1++;
  System.out.println(ch1+" "+ch2);

  

  
  
  

}
}